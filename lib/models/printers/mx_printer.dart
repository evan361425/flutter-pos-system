import 'dart:math';
import 'dart:typed_data';

import 'package:possystem/models/printer.dart';

///   - 0xAE01 - Write no response - used for printing
///   - 0xAE02 - Notify - receive notifications for commands 0xA3 and 0xA8
class MxPrinter extends PrinterImpl {
  const MxPrinter({
    super.serviceUuid = 0xAE30,
    super.writerChar = 0xAE01,
    super.readChar = 0xAE02,
  });

  @override
  Uint8List prepare() {
    return Uint8List.fromList(
      _wrap(_Cmd.controlLattice, [0xAA, 0x55, 0x17, 0x38, 0x44, 0x5F, 0x5F, 0x5F, 0x44, 0x38, 0x2C]) +
          _wrap(_Cmd.setMode, [0x00]) +
          _wrap(_Cmd.configFeedSpeed, [0x0a]),
    );
  }

  @override
  Uint8List draw(Uint8List image, {required int width, int padding = 0x2A}) {
    width ~/= 8;
    return Uint8List.fromList(
      [
            for (var i = 0; i < image.length; i += width)
              ..._wrap(_Cmd.draw, image.sublist(i, min(i + width, image.length)))
          ] +
          _wrap(_Cmd.feedPaper, _int2Bytes(padding)),
    );
  }

  List<int> _wrap(int cmd, List<int> data) {
    return [
      _Cmd.prefix1,
      _Cmd.prefix2,
      cmd,
      0x00, // direction, 0 for sending to printer, 1 for response from printer
      // length: 48 => 0x3000
      ..._int2Bytes(data.length),
      ...data,
      _calculateChecksum(data),
      0xff, // postfix
    ];
  }
}

///     - optional command(0xF2, default_value)          5178f200020001b410ff
///     - optional command(get_device_state, 0x00)       5178a30001000000ff
///     - optional command(set_quality, 0x34)            5178a4000100348cff
///     - optional command(control_lat, prefixLattice)   5178a6000b00aa551738445f5f5f44382ca1ff
///     - optional command(set_energy, 0x1027)           5178af0002001027a2ff
///     - required command(set_drawing_mode, 0x00)       5178be0001000000ff
///     - optional command(config_feed_speed, 0x0a)      5178bd0001000a36ff
///     - write image                                    5178a2003000..??ff
///     - ...                                            ...
///     - optional command(config_feed_speed, 0x0a);     5178bd0001000a36ff
///     - optional command(feed_paper, 0x3000);          5178a10002003000f9ff
///     - optional command(control_lat, postfixLattice); 5178a6000b00aa5517000000000000001711ff
///     - optional command(get_device_state, 0x00);      5178a30001000000ff
///     - optional command(get_device_state, 0x00);      5178a30001000000ff
class _Cmd {
  static const prefix1 = 0x51;
  static const prefix2 = 0x78;

  /// Get device state. 0x00 (get by subscription (0xAE02), 3 bytes, only first byte is meaningful)
  /// First byte means:
  /// - 1 - paper sensor (0x01) - paper not found or paper door open
  /// - 2 - cover (0x02)
  /// - 3 - head hot (0x04)
  /// - 4 - low battery (0x08)
  /// - 8 - in print progress (0x80)
  ///
  /// For example:
  /// - 0x01abcd - paper not found or paper door open
  /// - 0x001234 - all good
  static const getState = 0xA3;

  /// Get device info. 0x00 (get by subscription (0xAE02), return device version)
  /// For example:
  /// - 0x5120312E302E3131 - Q 1.0.11
  static const getDevice = 0xA8;

  /// Set quality. 0x31 - 0x36 looks all same, should be 0x33 or 0x34.
  static const setQuality = 0xA4;

  /// Set energy. 0x01 - 0xFFFF (0x0A before printing, 0x0F after printing)
  static const setEnergy = 0xAF;

  /// Set drawing mode. 0x00 (for images), 0x01 (for text)
  static const setMode = 0xBE;

  /// Draw line. 0 bit (don't draw pixel), 1 bit (draw pixel)
  static const draw = 0xA2;

  /// Retract paper (go inside the box). #(number of steps)
  static const retractPaper = 0xA0;

  /// Feed paper (go outside the box). #(number of steps)
  static const feedPaper = 0xA1;

  /// Config feed speed. 0x00 - 0xFFFF (speed of feeding, 0x1e before printing, 0x19 after printing)
  static const configFeedSpeed = 0xBD;

  /// Control Lattice-Eleven bytes. (always same bytes)
  /// see: https://en.wikipedia.org/wiki/Lattice-based_access_control
  static const controlLattice = 0xA6;

  /// Control line. 0x00 (On), 0x01 (Off)
  static const controlLine = 0xAE;
}

Uint8List _int2Bytes(int value) {
  return (Uint8List(4)..buffer.asInt32List()[0] = value).sublist(0, 2);
}

int _calculateChecksum(List<int> bytes) {
  var checksum = 0;
  for (final byte in bytes) {
    checksum = _checksumTable[checksum ^ byte];
  }

  return checksum;
}

